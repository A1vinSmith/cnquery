package docs

import (
	"fmt"
	"strings"
)

type goBuilder struct{}

func Go(packageName string, docs LrDocs) string {
	var sb strings.Builder
	sb.WriteString(fmt.Sprintf(goHeader, packageName))
	sb.WriteString(docs.MarshalGo())
	return sb.String()
}

const goHeader = `// Code generated by resources. DO NOT EDIT.
package %s

import (
	"go.mondoo.io/mondoo/resources/lr/docs"
)

`
