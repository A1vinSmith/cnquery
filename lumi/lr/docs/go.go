package docs

import "strings"

type goBuilder struct{}

func Go(docs LrDocs) string {
	var sb strings.Builder
	sb.WriteString(goHeader)
	sb.WriteString(docs.MarshalGo())
	return sb.String()
}

const goHeader = `// Code generated by lumi. DO NOT EDIT.
package resources

import (
	"go.mondoo.io/mondoo/lumi/lr/docs"
)

`
